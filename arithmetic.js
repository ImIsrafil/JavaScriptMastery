// JavaScript Arithmetic Operator
// +, -, *, /, **, %, ++, --,
{
  let x = 100 + 5;
  console.log(x);
}
{
  const a = 10;
  const b = 5;
  let x = a + b;
  console.log(x);
}
{
  const a = 3;
  let x = (100 + 50) * a;
  console.log(x);
}

// The Addition Operator (+)
{
  const a = 10;
  const b = 20;
  let x = a + b;
  console.log(x);
}

// Subtraction Operator (-)
{
  const a = 10;
  const b = 20;
  let x = a - b;
  console.log(x);
}

// Multiplecation Operator (*)
{
  const a = 10;
  const b = 20;
  let x = a * b;
  console.log(x);
}

// Divition Operator (/)
{
  const a = 10;
  const b = 2;
  let x = a / b;
  console.log(x);
}

// Exponentiation Operator (**)
{
  const a = 10;
  let x = a ** 2;
  console.log(x);
}

// Modulus Operator (%)
{
  const a = 15;
  const b = 4;
  let x = a % b;
  console.log(x);
}

// Increment Operator (++)
{
  let a = 10;
  a++;
  console.log(a);
}

// Decrement Operator (--)
{
  let a = 10;
  a--;
  console.log(a);
}
